package com.company.javaRelearn;

public class GenericMethods_2022_9_28 {
    public <T> void f(T x) {
        System.out.println(x.getClass().getSimpleName());
    }

    public static void main(String[] args) {
        GenericMethods_2022_9_28 gm = new GenericMethods_2022_9_28();
        gm.f("");
        gm.f(1);
        gm.f(1.0);
        gm.f(1.0F);
        gm.f('c');
        gm.f(gm);
    }
}
